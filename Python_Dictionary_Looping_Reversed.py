# Python Dictionary Looping Techniques (reversed ()).
# To loop over a sequence in reverse, first specify the sequence 
# in a forward direction and then call the reversed() function.

for i in reversed(range(1, 10, 2)):
      print(i)

# Displays '9'
# Displays '7'
# Displays '5'
# Displays '3'
# Displays '1'
